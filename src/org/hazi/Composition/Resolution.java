package org.hazi.Composition;

public class Resolution {
	private int inches;
	private String colorCode;
	public Resolution(int inches, String colorCode) {
		
		this.inches = inches;
		this.colorCode = colorCode;
	}
	public int getInches() {
		return inches;
	}
	public String getColorCode() {
		return colorCode;
	}
	

}
