package org.hazi.ArraysDemo.ArraysList;


public class ChallengeContacts {
	private String name;
	private String phoneNumber;
	public ChallengeContacts(String name, String phoneNumber) {
		super();
		this.name = name;
		this.phoneNumber = phoneNumber;
	}
	public String getName() {
		return name;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	
	
	
}
